    private void printCache(int key, int val) {
        CacheNode znode = this.head;
        System.out.print(key + ":" + val + "-->" + znode.key + ":" + znode.value + "| ");
        while (znode.next != null) {
            znode = znode.next;
            System.out.print(znode.key + ":" + znode.value + "| ");
        }
        System.out.println("");
        CacheNode fnode = this.tail;
        System.out.print(key + ":" + val + "-->" + fnode.key + ":" + fnode.value + "| ");
        while (fnode.pre != null) {
            fnode = fnode.pre;
            System.out.print(fnode.key + ":" + fnode.value + "| ");
        }
        System.out.println("");
        System.out.println("======================================================================================================");
    }

